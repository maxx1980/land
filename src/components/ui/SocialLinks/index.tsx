export function SocialLinks({
  links,
  size = 'md',
  variant = 'light',
}: SocialLinksProps) {
  if (links.length === 0) return null;

  const iconSize = size === 'sm' ? 'w-5 h-5' : 'w-6 h-6';

  return (
    <div className={cn('flex items-center', variantClasses[variant])}>
      {links.map((link) => {
        const href = resolveUrl(link.platform, link.url);
        const label = link.label || link.platform;
        const isExternal = link.platform !== 'email';
        const d = iconPaths[link.platform];

        return (
          <a
            key={`${link.platform}-${link.url}`}
            href={href}
            aria-label={label}
            className={cn(
              'inline-flex items-center justify-center transition-colors',
              sizeClasses[size],
              variantClasses[variant],
            )}
            {...(isExternal
              ? { target: '_blank', rel: 'noopener noreferrer' }
              : {})}
          >
            {d ? (
              <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className={iconSize} aria-hidden="true">
                <path d={d} />
              </svg>
            ) : (
              <span className="text-xs font-bold uppercase">{link.platform.slice(0, 2)}</span>
            )}
          </a>
        );
      })}
    </div>
  );
}
